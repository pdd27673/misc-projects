// All timestamps are UTC. Base date: 2026-06-11 (Thursday)
// t() produces an ISO string offset from a base date by hours
function t(dateStr: string, hour: number, min = 0): string {
  const d = new Date(`${dateStr}T00:00:00Z`);
  d.setUTCHours(hour, min, 0, 0);
  return d.toISOString();
}

function end(start: string, durationHours: number): string {
  const d = new Date(start);
  d.setTime(d.getTime() + durationHours * 60 * 60 * 1000);
  return d.toISOString();
}

const NOW = new Date();
export const MODULE_LOAD_TIME = NOW.getTime();
function fromNow(offsetMinutes: number): string {
  const d = new Date(NOW.getTime() + offsetMinutes * 60 * 1000);
  return d.toISOString();
}

