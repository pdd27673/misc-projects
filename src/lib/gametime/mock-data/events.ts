export function getEventsForDate(dateStr: string): SportEvent[] {
  return MOCK_EVENTS.filter(e => e.startTimeUtc.startsWith(dateStr));
}

export function getLiveEvents(): SportEvent[] {
  return MOCK_EVENTS.filter(e => e.isLive || e.status === 'live');
}

export function getUpcomingEvents(withinMinutes = 180): SportEvent[] {
  const cutoff = new Date(Date.now() + withinMinutes * 60 * 1000);
  const now = new Date();
  return MOCK_EVENTS.filter(e => {
    const start = new Date(e.startTimeUtc);
    return !e.isLive && start > now && start <= cutoff;
  });
}

export function getFeaturedEvents(): SportEvent[] {
  return MOCK_EVENTS.filter(e => e.isFeatured);
}
